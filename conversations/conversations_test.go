func (m *mockRequester) Delete(ctx context.Context, path string) error {
	req, err := http.NewRequestWithContext(ctx, http.MethodDelete, m.server.URL+path, nil)
	if err != nil {
		return err
	}
	return m.doRequest(req, nil)
}

func (m *mockRequester) doRequest(req *http.Request, result any) error {
	resp, err := http.DefaultClient.Do(req)
	if err != nil {
		return err
	}
	defer resp.Body.Close()
	if result != nil {
		body, _ := io.ReadAll(resp.Body)
		if len(body) > 0 {
			return json.Unmarshal(body, result)
		}
	}
	return nil
}

func setupConversations(t *testing.T) (*conversations.Service, *http.ServeMux, func()) {
	t.Helper()
	mux := http.NewServeMux()
	server := httptest.NewServer(mux)
	requester := &mockRequester{server: server}
	svc := conversations.NewService(requester)
	return svc, mux, server.Close
}

func mustJSON(t *testing.T, v any) []byte {
	t.Helper()
	b, err := json.Marshal(v)
	if err != nil {
		t.Fatalf("failed to marshal JSON: %v", err)
	}
	return b
}

func TestVisitorIdentification_GenerateToken(t *testing.T) {
	svc, mux, teardown := setupConversations(t)
	defer teardown()

	mux.HandleFunc("/conversations/v3/visitor-identification/tokens/create", func(w http.ResponseWriter, r *http.Request) {
		if r.Method != http.MethodPost {
			t.Errorf("method = %s, want POST", r.Method)
		}
		body, _ := io.ReadAll(r.Body)
		var input conversations.IdentificationTokenGenerationRequest
		_ = json.Unmarshal(body, &input)
		if input.Email != "visitor@example.com" {
			t.Errorf("email = %q", input.Email)
		}
		if input.FirstName != "Jane" {
			t.Errorf("firstName = %q", input.FirstName)
		}
		w.WriteHeader(http.StatusOK)
		_, _ = w.Write(mustJSON(t, conversations.IdentificationTokenResponse{
			Token: "tok_abc123",
		}))
	})

	result, err := svc.VisitorIdentification.GenerateToken(context.Background(), &conversations.IdentificationTokenGenerationRequest{
		Email:     "visitor@example.com",
		FirstName: "Jane",
		LastName:  "Doe",
	})
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	if result.Token != "tok_abc123" {
		t.Errorf("token = %q, want tok_abc123", result.Token)
	}
}
